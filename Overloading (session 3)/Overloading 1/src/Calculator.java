public class Calculator {

    // جمع دو عدد صحیح
    public int sum(int a, int b) {
        return a + b;
    }

    // جمع سه عدد صحیح
    public int sum(int a, int b, int c) {
        return a + b + c;
    }

    // جمع دو عدد اعشاری
    public double sum(double a, double b) {
        return a + b;
    }

    // جمع یک عدد صحیح و یک اعشاری
    public double sum(int a, double b) {
        return a + b;
    }
}